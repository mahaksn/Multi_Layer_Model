% function [w,p]=RefElemQuad(order)
% switch order
%     case 1
%         w=1/2;
%         p=[1/3;1/3];
%     case 2
%         w=(1/6)*[1,1,1];
%         p=[0,0.5,0.5;0.5,0,0.5];
%     case 3
%         w=(1/48)*[-27,25,25,25];
%         p=[1/3,2/15,2/15,11/15;...
%             1/3,11/15,2/15,2/15];
%     case 4
%         w1=0.22338158967801;
%         w2=0.10995174365532;
%         w=[w1,w1,w1,w2,w2,w2];
%         a=0.4459849091597;
%         b=0.10810301816807;
%         c=0.09157621350977;
%         d=0.81684757298046;
%         p=[q,q,b,c,c,d;a,b,a,c,d,c];
%     case 5
%         w2=0.13239415278851;
%         w3=0.12593918054483;
%         w=[0.225,w2,w2,w2,w3,w3,w3];
%         a=0.47014206410511;
%         b=0.05971587178977;
%         c=0.10128650732346;
%         d=0.79742698535309;
%         p=[1/3,a,a,b,c,c,d;...
%             1/3,a,b,a,c,d,c];
%     otherwise
%         error('wrongly defined');
% end

function [w,p]=RefElemQuad(order)
switch order
    case 1
        w=1/2;
        p=[1/3;1/3];
    case 2
        w=(1/6)*[1,1,1];
        p=[0,0.5,0.5; 0.5,0,0.5];
    case 3
        w=(1/48)*[-27,25,25,25];
        p=[1/3,  2/15, 2/15, 11/15; ...
           1/3, 11/15, 2/15,  2/15];
    case 4
        w1=0.22338158967801;
        w2=0.10995174365532;
        w=[w1,w1,w1,w2,w2,w2];
        a=0.4459849091597;
        b=0.10810301816807;
        c=0.09157621350977;
        d=0.81684757298046;
        p=[a, a, b, c, c, d; ...
           a, b, a, c, d, c];
    case 5
        w2=0.13239415278851;
        w3=0.12593918054483;
        w=[0.225,w2,w2,w2,w3,w3,w3];
        a=0.47014206410511;
        b=0.05971587178977;
        c=0.10128650732346;
        d=0.79742698535309;
        p=[1/3, a, a, b, c, c, d; ...
           1/3, a, b, a, c, d, c];
    otherwise
        error('RefElemQuad: order not implemented');
end
end
